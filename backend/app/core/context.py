"""Технический контекст пользователя платформы.
Файл существует, чтобы стандартизировать минимальные данные о текущем пользователе.
Минимальность: только идентификатор и имя без бизнес-полей и прав.
"""

from pydantic import BaseModel


class UserContext(BaseModel):
    """Минимальный контекст текущего пользователя.
    Используется во всех модулях как единый технический формат.
    """

    id: int
    username: str
